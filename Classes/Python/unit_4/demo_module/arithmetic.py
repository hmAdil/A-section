def add(a,b):
    '''This function takes 2 numeric values a and b, and then it gives sum of them.'''
    return a+b
def sub(a,b):
    '''This function takes 2 numeric values a and b, and then it gives difference of them.'''
    return a-b
def mul(a,b):
    '''This function takes 2 numeric values a and b, and then it gives product of them.'''
    return a*b
def div(a,b):
    '''This function takes 2 numeric values a and b, and then it gives quotient of them.'''
    return a/b


